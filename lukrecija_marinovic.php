								Rođena 1975. god u Dubrovniku gdje završava osnovnu i srednju školu.<br><br>
2000. god. diplomirala je na temu „Atopijski dermatitis u pasa“ na Zavodu za mikrobiologiju i zarazne bolesti Veterinarskog fakulteta u Zagrebu, nakon čega se na istom zavodu zapošljava kao znanstveni novak. 2002. god. U sklopu znanstveno-istraživačkog projekta na kojem radi dobiva stipendiju francuske vlade za tromjesečni rad na Veterinarskom fakultetu u Nantesu u Francuskoj.<br><br>
2003. vraća se u Dubrovnik gdje radi u Specijaliziranoj ambulanti za male životinje Bobanović.<br><br>  
2006. god odlazi u francusku na šestomjesečno stručno usavršavanje iz područja bolesti kućnih ljubimaca na klinikama Veterinarskog fakulteta u Nantesu.<br><br>  
2010. godine odlazi u Zagreb i radi u Veterinarskoj ambulanti Šapa do 2013, zatim Veterinarskoj ambulanti Nera.<br><br> 
2017 god. odlazi na jednogodišnji specijalistički staž iz dermatologije uz rad u svojstvu kliničkog asistenta na veterinarskom fakultetu Toulouse, Francuska.<br><br> 
